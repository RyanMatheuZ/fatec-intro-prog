// Function declaration

function sayHello() {
    console.log('Hello')
}
sayHello()

function sayHelloTo(name) {
    console.log(`Hello, ${name}!`)
}
sayHelloTo('Ryan Matheus')

function returnHi() {
    return 'Hi!'
}
console.log(returnHi())

function returnHiToo(name) {
    return `Hi, ${name}!`
}
console.log(returnHiToo('Ryan'))