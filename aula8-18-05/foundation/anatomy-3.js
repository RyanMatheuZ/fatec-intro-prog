// Function expression

const increment1 = function(n) {
    return n + 1
}
console.log(increment1(14))

// Arrow Function is always anonymous

const increment2 = (n) => {
    return n + 1
}
console.log(increment2(08))

const increment3 = n => n + 1
console.log(increment3(03))

const sum = (a, b) => a + b
console.log(sum(14, 08))